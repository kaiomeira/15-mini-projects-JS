// Declaring Variables (Declarando Variáveis)
let color1, color2;

// Assigning Values to Variables (Atribuindo valores p/ variáveis)
color1 = 'Red';
color2 = 'Blue'

console.log(color1);
console.log(color2);

// Declaring Variables Auxiliary (Declarando Variávei Auxiliar)
let aux = color1;
color1 = color2;
color2 = aux;

console.log(color1);
console.log(color2);